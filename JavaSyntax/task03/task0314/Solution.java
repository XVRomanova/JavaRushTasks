package com.javarush.task.task03.task0314;

/* 
Таблица умножения
*/

public class Solution {
    public static void str(int k) {
        for (int i = 1; i <=10; i++) {
            System.out.print (k*i + " ");
        }
        System.out.println("");

    }
    public static void main(String[] args) {
        str(1);
        str(2);
        str(3);
        str(4);
        str(5);
        str(6);
        str(7);
        str(8);
        str(9);
        str(10);



    }
}
